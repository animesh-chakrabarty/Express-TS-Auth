import mongoose, { Model } from "mongoose";
import { userInterface } from "../interfaces/user.interfaces";

export const userSchema = new mongoose.Schema<userInterface>({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  isVerified: {
    type: Boolean,
    required: true,
    default: false,
  },
});
